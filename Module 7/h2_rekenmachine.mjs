let rekenmachine = {
    optellen(getal1, getal2) {
        let result = getal1 + getal2
        return result},
    aftrekken(getal1, getal2) {
        let result = getal1 - getal2
        return result},
    vermenigvuldigen(getal1, getal2) {
        let result = getal1 * getal2
        return result},
    delen(getal1, getal2) {
        let result = getal1 / getal2
        return result
    }
}


